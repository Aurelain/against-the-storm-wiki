import getAsset from './getAsset.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const getScriptPath = (asset, database) => {
    const scriptAsset = getAsset(asset.m_Script, database);
    return scriptAsset.shortPath;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default getScriptPath;
