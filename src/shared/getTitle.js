import assert from 'assert/strict';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const getTitle = (props, database) => {
    const {texts} = database;
    const title = texts[props.displayName?.key];
    assert(title, `Falsy title @ "${props.m_Name}"!`);
    return title;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default getTitle;
